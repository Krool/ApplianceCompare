function applyMatches(category, rows) {
  const file = path.join(ROOT, 'public', 'data', category + '.json');
  const data = JSON.parse(fs.readFileSync(file, 'utf8'));
  let updated = 0;
  let setReliability = 0;
  rows.forEach(([modelStr, overall, rel]) => {
    const target = norm(modelStr);
    const m = data.models.find(x => norm(x.model) === target);
    if (!m) return;
    m.ratings = m.ratings || {};
    m.ratings.source_urls = m.ratings.source_urls || {};
    if (overall != null) m.ratings.cr_overall = overall;
    // Only set categorical when not already set to a more granular value
    if (rel && rel !== '-' && (!m.ratings.cr_reliability || m.ratings.cr_reliability === 'unrated')) {
      m.ratings.cr_reliability = (rel === 'BETTER') ? 'very-good' : 'fair';
      setReliability++;
    } else if (rel === '-' && !m.ratings.cr_reliability) {
      m.ratings.cr_reliability = 'unrated';
    }
    if (!m.ratings.source_urls.cr) {
      m.ratings.source_urls.cr = 'https://www.consumerreports.org/cro/' + category + '/buying-guide/index.htm';
    }
    updated++;
  });
  if (data._meta) {
    data._meta.last_updated = '2026-04-25';
    if (data._meta.sources && typeof data._meta.sources === 'object' && !Array.isArray(data._meta.sources)) {
      data._meta.sources.cr_2026_buying_guide = 'https://www.consumerreports.org/cro/buying-guide/index.htm';
    }
  }
  fs.writeFileSync(file, JSON.stringify(data, null, 2) + '\n');
  return { updated, setReliability };
}

